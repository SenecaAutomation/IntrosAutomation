package com.seneca.thucydides.util;

public class TestOutcomeDetails {
	
	private String requirementId;
	private String story;
	private String testClassName;
	private String testDescription;
	private String testType;
	private String testRowNo;
	private String testResult;
	private String testExecute;
	
	public String getTestExecute() {
		return testExecute;
	}
	public void setTestExecute(String testExecute) {
		this.testExecute = testExecute;
	}
	public String getTestResult() {
		return testResult;
	}
	public void setTestResult(String testResult) {
		this.testResult = testResult;
	}
	public String getTestRowNo() {
		return testRowNo;
	}
	public void setTestRowNo(String testRowNo) {
		this.testRowNo = testRowNo;
	}
	public String getTestType() {
		return testType;
	}
	public void setTestType(String testType) {
		this.testType = testType;
	}
	public String getTestDescription() {
		return testDescription;
	}
	public void setTestDescription(String testDescription) {
		this.testDescription = testDescription;
	}
	public String getTestClassName() {
		return testClassName;
	}
	public void setTestClassName(String testClassName) {
		this.testClassName = testClassName;
	}
	public String getRequirementId() {
		return requirementId;
	}
	public void setRequirementId(String requirementId) {
		this.requirementId = requirementId;
	}
	public String getStory() {
		return story;
	}
	public void setStory(String story) {
		this.story = story;
	}

}
